SELECT DTL.YEARMONTH,
       DTL.INVOICE_NUMBER_GK,
       DTL.PRICE_CODE,
       DTL.ORIG_PRICE_CODE,
       DTL.INVOICE_LINE_NUMBER,
       DTL.PRODUCT_GK,
       DTL.SPECIAL_PRODUCT_GK,
       DTL.EXT_AVG_COGS_AMOUNT,
       DTL.CORE_ADJ_AVG_COST,
       DTL.EXT_ACTUAL_COGS_AMOUNT,
       DTL.EXT_SALES_AMOUNT,
       DTL.PRICE_CATEGORY,
       DTL.PRICE_CATEGORY_OVR_PR,
       DTL.PRICE_CATEGORY_OVR_GR,
       DTL.INSERT_TIMESTAMP,
       DTL.PROCESS_DATE,
       DTL.PRICE_FORMULA,
       HIST.INVOICE_NUMBER_GK,
       HIST.INVOICE_LINE_NUMBER
FROM AAA6863.PR_VICT2_CUST_12MO DTL
     LEFT OUTER JOIN PRICE_MGMT.PR_PRICE_CAT_HISTORY HIST
        ON     (DTL.INVOICE_NUMBER_GK = HIST.INVOICE_NUMBER_GK)
           AND (DTL.INVOICE_LINE_NUMBER = HIST.INVOICE_LINE_NUMBER)
WHERE (HIST.INVOICE_NUMBER_GK IS NULL) AND (HIST.INVOICE_LINE_NUMBER IS NULL)