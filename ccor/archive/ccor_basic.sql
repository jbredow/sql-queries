SELECT COD.BASIS, 
       COD.BRANCH_NUMBER_NK, 
       COD.CONTRACT_ID, 
       COD.CUSTOMER_GK, 
       COD.CUSTOMER_NK, 
       COD.DISC_GROUP, 
       COD.INSERT_TIMESTAMP, 
       COD.EXPIRE_DATE, 
       COD.MASTER_PRODUCT, 
       COD.MULTIPLIER, 
       COD.OPERATOR_USED, 
       COD.OVERRIDE_ID_NK, 
       COD.OVERRIDE_TYPE, 
       COD.BASIS || COD.OPERATOR_USED || '0' || COD.MULTIPLIER FORMULA 
  FROM DW_FEI.CUSTOMER_OVERRIDE_DIMENSION COD 
 WHERE     COD.OVERRIDE_TYPE = 'G' 
       AND COD.DELETE_DATE IS NULL 
       AND COD.DISC_GROUP = '&INPUT_DG'
