SELECT DISTINCT TPD.YEARMONTH,
                SWD.DIVISION_NAME AS REGION,
                SWD.REGION_NAME AS DISTRICT,
                SWD.REGION_NAME AS DISTRICT2,
                SWD.WAREHOUSE_NUM_NAME AS WAREHOUSE,
                MP_DET.ALT1_CODE_AND_PRODUCT_NAME AS ALT1_DESC,
                MP_DET.PRODUCT_NK,
                MP_DET.WRITER,
                MP_DET.WRITER AS WRITER2,
                MP_DET.PRICE_CATEGORY,
                SUM (MP_DET.SHIPPED_QTY) SHIPPED,
                SUM (MP_DET.EXT_SALES_AMOUNT) EXT_SALES,
                SUM (MP_DET.EXT_ACTUAL_COGS_AMOUNT) EXT_REP_COST
  FROM (SALES_MART.PRICE_MGMT_DATA_DET MP_DET
        INNER JOIN SALES_MART.SALES_WAREHOUSE_DIM SWD
           ON (MP_DET.SHIP_WAREHOUSE_NUMBER_NK = SWD.WAREHOUSE_NUMBER_NK))
       INNER JOIN SALES_MART.TIME_PERIOD_DIMENSION TPD
          ON (MP_DET.YEARMONTH = TPD.YEARMONTH)
 WHERE     (TPD.FISCAL_YEAR_TO_DATE = 'YEAR TO DATE')
       AND (MP_DET.DISCOUNT_GROUP_NK = '9181')
       --AND (MP_DET.ACCOUNT_NUMBER_NK = '2000')
GROUP BY TPD.FISCAL_YEAR_TO_DATE,
         SWD.DIVISION_NAME,
         SWD.REGION_NAME,
         SWD.WAREHOUSE_NUM_NAME,
         MP_DET.ALT1_CODE_AND_PRODUCT_NAME,
         MP_DET.PRODUCT_NK,
         MP_DET.WRITER,
         MP_DET.PRICE_CATEGORY,
         MP_DET.DISCOUNT_GROUP_NK,
         MP_DET.ACCOUNT_NUMBER_NK,
         MP_DET.YEARMONTH,
         TPD.YEARMONTH