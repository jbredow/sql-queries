SELECT PROD.ALT1_CODE,
		PROD.PRODUCT_NAME,
		PROD.PRODUCT_NK,
		PROD.SELL_MULT,
		PROD.DISCOUNT_GROUP_NK,
		
		BR_DG.ACCOUNT_NAME,
		BR_DG.ACCOUNT_NUMBER_NK,
		BR_DG.DISC_TO_COST,
		BR_DG.REP_COST_OK,
		
		PRICE.BASIS,
		PRICE.MULTIPLIER,
		PRICE.OPERATOR_USED,
		PRICE.PRICE_COLUMN,
		PRICE.PRICE_GK,

		WHSE.ALIAS_NAME,
		WHSE.WAREHOUSE_GK,

		WH_PR.BASIS_2,
		WH_PR.ON_HAND_QTY,
		WH_PR.PI_COST,
		WH_PR.UM_CODE,
		WH_PR.WHSE_AVG_COST_AMOUNT

FROM 	DW_FEI.PRODUCT_DIMENSION PROD,
		DW_FEI.PRICE_DIMENSION PRICE,
		DW_FEI.BRANCH_DISC_GROUP_DIMENSION BR_DG,
		DW_FEI.WAREHOUSE_PRODUCT_FACT WH_PR,
		DW_FEI.WAREHOUSE_DIMENSION WHSE
		
WHERE PROD.PRODUCT_GK = WH_PR.PRODUCT_GK
	AND WHSE.WAREHOUSE_GK = WH_PR.WAREHOUSE_GK
	AND WHSE.ACCOUNT_NUMBER_NK = PRICE.BRANCH_NUMBER_NK
	AND PRICE.BRANCH_NUMBER_NK = BR_DG.ACCOUNT_NUMBER_NK
	AND BR_DG.ACCOUNT_NUMBER_NK IN (
		'448','520','1550','1674','3093','226','1020','1657',
		'331','150','56','215','13','100','2000','20','1480',
		'1589','420','564','216',
		'1869','116','61','190','480','230','454','88','1895',
		'788','1116','1105','1221','1616','2721','3083','8423',
		'2637','1701','1423','3011','1539','1083','1600','1934',
		'1491','2516','1476')
	AND PROD.DISCOUNT_GROUP_NK = '1420'
	
ORDER BY BR_DG.ACCOUNT_NAME,
		--WHSE.WAREHOUSE_NK,
		PROD.DISCOUNT_GROUP_NK,
		PROD.ALT1_CODE;