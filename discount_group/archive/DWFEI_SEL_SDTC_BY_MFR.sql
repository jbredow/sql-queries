SELECT DISTINCT 
			PR_MFG_DG_XREF.MASTER_VENDOR_NAME,
			CONTACTS.RPC,
			DC_SDTC.DIVISION_NAME,
			DC_SDTC.REGION_NAME,
			DC_SDTC.ACCOUNT_NAME,
			DC_SDTC.BRANCH_NK,
			DC_SDTC.CUSTOMER_ALPHA,
			DC_SDTC.CUSTOMER_NAME,
			DC_SDTC.PC,
			DC_SDTC.DISC_GRP,
			DC_SDTC.DISCOUNT_GROUP_NAME,
			DC_SDTC.BASIS,
			DC_SDTC.OPER,
			DC_SDTC.MULT_MODE,
			DC_SDTC.BR_SDTC,
			DC_SDTC.LAST_UPDATE,
			DC_SDTC.DELETE_DATE,
			BR_DG.DISC_TO_COST,
			BR_DG.RAW_DISC_TO_COST
  FROM    (      AAD9606.PR_DC_SRC_SDTC DC_SDTC
              INNER JOIN
                 AAD9606.PR_MFG_DG_XREF PR_MFG_DG_XREF
              ON (LTRIM(DC_SDTC.DISC_GRP,'0') =
                     LTRIM (PR_MFG_DG_XREF.DISC_GRP, '0'))
           
		   LEFT OUTER JOIN
              DW_FEI.BRANCH_DISC_GROUP_DIMENSION BR_DG
           ON (DC_SDTC.ACCOUNT_NAME = BR_DG.ACCOUNT_NAME)
              AND (DC_SDTC.DISC_GRP = BR_DG.BRANCH_DISC_GROUP_NK))
       
	   INNER JOIN
          USER_SHARED.BRANCH_CONTACTS CONTACTS
       ON (DC_SDTC.BRANCH_NK = CONTACTS.ACCOUNT_NUMBER_NK)
 WHERE (PR_MFG_DG_XREF.MASTER_VENDOR_NAME LIKE '%' || :VENDOR_NAME || '%')
       AND RPC = :RPC_REGION
	   ;